package data;


public class DataInfo {

	private int totDomande;
	private int domandaCorrente;
	private int giuste;
	private int errate;
	public int getTotDomande() {
		return totDomande;
	}
	public void setTotDomande(int totDomande) {
		this.totDomande = totDomande;
	}
	public int getDomandaCorrente() {
		return domandaCorrente;
	}
	public void setDomandaCorrente(int domandaCorrente) {
		this.domandaCorrente = domandaCorrente;
	}
	public int getGiuste() {
		return giuste;
	}
	public void setGiuste(int giuste) {
		this.giuste = giuste;
	}
	public int getErrate() {
		return errate;
	}
	public void setErrate(int errate) {
		this.errate = errate;
	}
	

}
